				<p>&nbsp;</p>
				<p>Kalau kita mempelajari sejarah pendidikan kepramukaan kita tidak dapat lepas dari riwayat hidup pendiri gerakan kepramukaan sedunia Lord Robert Baden Powell of Gilwell.</p>
				<p>Hal ini disebabkan pengalaman beliaulah yang mendasari pembinaan remaja di negara Inggris. Pembinaan remaja inilah yang kemudian tumbuh berkembang menjadi gerakan kepramukaan.</p>
				<p>&nbsp;</p>
				<p>B.  Riwayat hidup Baden Powell</p>
				<p>&nbsp;</p>
				<p>Lahir tanggal 22 Pebruari 1857 dengan nama Robert Stephenson Smyth. Ayahnya bernama powell seorang Professor Geometry di Universitas Oxford, yang meninggal ketika Stephenson masih kecil.</p>
				<p>Pengalaman Baden Powell yang berpengaruh pada kegiatan kepramukaan banyak sekali dan menarik diantaranya :</p>
				<ol>
				<li>Karena ditinggal bapak sejak kecil, maka mendapatkan pembinaan watak ibunya.</li>
				<li>Dari kakaknya mendapat latihan keterampilan berlayar, berenang, berkemah, olah raga dan lain-lainnya.</li>
				<li>Sifat Baden Powell yang sangat cerdas, gembira, lucu, suka main musik, bersandiwara, berolah raga, mengarang dan menggambar sehingga disukai teman-temannya.</li>
				<li>Pengalaman di India sebagai pembantu Letnan pada Resimen 13 Kavaleri yang berhasil mengikuti jejak kuda yang hilang di puncak gunung serta keberhasilan melatih panca indera kepada Kimball O’Hara.</li>
				<li>Terkepung bangsa Boer di kota Mafeking, Afrika Selatan selama 127 hari dan kekurangan makan.</li>
				<li>Pengalaman mengalahkan Kerajaan Zulu di Afrika dan mengambil kalung manik kayu milik Raja Dinizulu.</li>
				</ol>
				<p>&nbsp;</p>
				<p>Pengalaman ini ditulis dalam buku “Aids To Scouting” yang merupakan petunjuk bagi Tentara muda Inggris agar dapat melaksanakan tugas penyelidik dengan baik. William Smyth seorang pimpinan Boys Brigade di Inggris minta agar Baden Powell melatih anggotanya sesuai dengan pengalaman beliau itu. Kemudian dipanggil 21 pemuda dari Boys Brigade di berbagai wilayah Inggris, diajak berkemah dan berlatih di pulau Browns Sea pada tanggal 25 Juli 1907 selama 8 hari.</p>
				<p>&nbsp;</p>
				<p>Tahun 1910 BP pensiun dari tentara dengan pangkat terakhir Letnan Jenderal. Pada tahun 1912 menikah dengan Ovale St. Clair Soames dan dianugerahi 3 orang anak. Beliau mendapat titel Lord dari Raja George pada tahun 1929 Baden Powell meninggal tanggal 8 Januari 1941 di Nyeri, Kenya, Afrika.</p>
