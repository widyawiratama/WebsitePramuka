				<p>C.  Sejarah Kepramukaan Sedunia</p>
				<p>&nbsp;</p>
				<p>Awal tahun 1908 Baden Powell menulis pengalamannya untuk acara latihan kepramukaan yang dirintisnya. Kumpulan tulisannya ini dibuat buku dengan judul “Scouting For Boys”. Buku ini cepat tersebar di Inggris dan negara-negara lain yang kemudian berdiri organisasi kepramukaan yang semula hanya untuk laki-laki dengan nama Boys Scout.</p>
				<p>&nbsp;</p>
				<p style="text-align: left;">            Tahun 1912 atas bantuan adik perempuan beliau, Agnes didirikan organisasi kepramukaan untuk wanita dengan nama Girl Guides yang kemudian diteruskan oleh istri beliau.</p>
				<p>&nbsp;</p>
				<p>Tahun 1916 berdiri kelompok pramuka usia siaga dengan nama CUB (anak serigala) dengan buku The Jungle Book karangan Rudyard Kipling sebagai pedoman kegiatannya. Buku ini bercerita tentang Mowgli si anak rimba yang dipelihara di hutan oleh induk serigala.</p>
